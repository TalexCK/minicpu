def run_tests():
    total = len(TEST_VECTORS)
    passed = 0
    for name, asm, expected in TEST_VECTORS:
        got = encode_code(asm)

        if isinstance(got, int):
            got_str = format(got & 0xFFFFFFFF, "032b")
        else:
            got_str = str(got)

        ok = got_str == expected
        if ok:
            passed += 1
        else:
            status = "FAIL"
            print(
                f"[{status}] {name:6s}  {asm:30s}  expected={expected}  got={got_str}"
            )

    print(f"\n{passed}/{total} tests passed")


if __name__ == "__main__":
    run_tests()
